package edu.mx.utdelacosta.model.dtoreport;

public class AsistenciasCorteDTO {
	
	private Integer idCorte;
	private Integer asistencias;
	private Integer faltas;
	private Integer justificantes;
	private Integer retardos;
	
	public Integer getIdCorte() {
		return idCorte;
	}
	public void setIdCorte(Integer idCorte) {
		this.idCorte = idCorte;
	}
	public Integer getAsistencias() {
		return asistencias;
	}
	public void setAsistencias(Integer asistencias) {
		this.asistencias = asistencias;
	}
	public Integer getFaltas() {
		return faltas;
	}
	public void setFaltas(Integer faltas) {
		this.faltas = faltas;
	}
	public Integer getJustificantes() {
		return justificantes;
	}
	public void setJustificantes(Integer justificantes) {
		this.justificantes = justificantes;
	}
	public Integer getRetardos() {
		return retardos;
	}
	public void setRetardos(Integer retardos) {
		this.retardos = retardos;
	}
	
}